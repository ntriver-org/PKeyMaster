function ConvertFrom-EncryptedTrustedStore([byte[]]$Raw, [byte[]]$RsaKey) {
    # Decrypt the store blob with the given RSA key + AES-CBC; returns $null on failure.
    if ($Raw.Length -lt 0x110) {
        return $null
    }

    $signature = New-Object byte[] 0x80
    $encryptedAesKey = New-Object byte[] 0x80
    $cipherText = New-Object byte[] ($Raw.Length - 0x110)

    [System.Buffer]::BlockCopy($Raw, 0x10, $signature, 0, $signature.Length)
    [System.Buffer]::BlockCopy($Raw, 0x90, $encryptedAesKey, 0, $encryptedAesKey.Length)
    [System.Buffer]::BlockCopy($Raw, 0x110, $cipherText, 0, $cipherText.Length)

    $rsa = $null
    $sha1 = $null
    $aes = $null

    try {
        $rsa = New-Object System.Security.Cryptography.RSACryptoServiceProvider
        $rsa.PersistKeyInCsp = $false
        $rsa.ImportCspBlob($RsaKey)

        $sha1 = [System.Security.Cryptography.SHA1]::Create()
        $deformatter = New-Object System.Security.Cryptography.RSAPKCS1SignatureDeformatter -ArgumentList $rsa
        $deformatter.SetHashAlgorithm('SHA1')

        if (-not $deformatter.VerifySignature($sha1.ComputeHash($encryptedAesKey), $signature)) {
            return $null
        }

        $aesKey = $rsa.Decrypt($encryptedAesKey, $false)

        $aes = New-Object System.Security.Cryptography.RijndaelManaged
        $aes.Key = $aesKey
        $aes.IV = New-Object byte[] 16
        $aes.Mode = [System.Security.Cryptography.CipherMode]::CBC
        $aes.Padding = [System.Security.Cryptography.PaddingMode]::PKCS7

        $plain = $aes.CreateDecryptor().TransformFinalBlock($cipherText, 0, $cipherText.Length)
        if ($plain.Length -le 0x28) {
            return $null
        }

        $store = New-Object byte[] ($plain.Length - 0x28)
        [System.Buffer]::BlockCopy($plain, 0x28, $store, 0, $store.Length)
        return , $store
    }
    catch {
        return $null
    }
    finally {
        if ($aes) {
            $aes.Clear()
        }
        if ($sha1) {
            $sha1.Clear()
        }
        if ($rsa) {
            $rsa.Clear()
        }
    }
}

